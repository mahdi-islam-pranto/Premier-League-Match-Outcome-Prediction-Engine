# Single shared pipeline instance — artifacts loaded once on first request
pipeline = PredictPipeline()



# PYDANTIC SCHEMAS
# API input
class PredictRequest(BaseModel):
    home_team:  str
    away_team:  str
    match_date: str = str(date.today())

    model_config = {"json_schema_extra": {
        "examples": [{
            "home_team":  "Arsenal",
            "away_team":  "Chelsea",
            "match_date": "2026-08-16"
        }]
    }}

    @field_validator('match_date')
    @classmethod
    def validate_date(cls, v: str) -> str:
        try:
            datetime.strptime(v, '%Y-%m-%d')
        except ValueError:
            raise ValueError("match_date must be in YYYY-MM-DD format.")
        return v

    @model_validator(mode='after')
    def teams_must_differ(self) -> 'PredictRequest':
        if self.home_team.strip().lower() == self.away_team.strip().lower():
            raise ValueError("home_team and away_team must be different.")
        return self


# API input for batch request
class BatchPredictRequest(BaseModel):
    matches: List[PredictRequest]

    model_config = {"json_schema_extra": {
        "examples": [{
            "matches": [
                {"home_team": "Arsenal",    "away_team": "Chelsea",   "match_date": "2026-08-16"},
                {"home_team": "Liverpool",  "away_team": "Man City",  "match_date": "2026-08-16"},
                {"home_team": "Man United", "away_team": "Tottenham", "match_date": "2026-08-17"},
            ]
        }]
    }}

    @field_validator('matches')
    @classmethod
    def limit_batch_size(cls, v):
        if len(v) == 0:
            raise ValueError("matches list cannot be empty.")
        if len(v) > 10:
            raise ValueError("Batch size is capped at 10 matches per request.")
        return v


# OUTPUT Schemas
class ProbabilitySchema(BaseModel):
    home_win: float
    draw:     float
    away_win: float


class EloSchema(BaseModel):
    home: float
    away: float
    diff: float


class PredictResponse(BaseModel):
    home_team:        str
    away_team:        str
    match_date:       str
    predicted_result: str
    predicted_label:  str
    probabilities:    ProbabilitySchema
    confidence:       float
    elo_ratings:      EloSchema
    features_used:    Dict[str, float]


class HealthResponse(BaseModel):
    status:         str
    model_loaded:   bool
    teams_available:int
    generated_at:   Optional[str]



# HELPERS (functions)


def _run_prediction(req: PredictRequest) -> Dict:
    """Shared logic used by both /predict and /predict/batch."""
    home = req.home_team.strip()
    away = req.away_team.strip()

    # Warn if team is unknown (pipeline handles it gracefully, but surface it)
    known = pipeline.available_teams()
    warnings = []
    if home not in known:
        warnings.append(
            f"'{home}' not found in training data. "
            "Prediction uses league-average defaults. "
            f"Known teams: {', '.join(known[:5])} …"
        )
    if away not in known:
        warnings.append(
            f"'{away}' not found in training data. "
            "Prediction uses league-average defaults."
        )

    result = pipeline.predict(
        home_team  = home,
        away_team  = away,
        match_date = req.match_date,
    )

    if warnings:
        result['warnings'] = warnings

    return result



# Define API ENDPOINTS

@app.get("/", tags=["General"], summary="Welcome")
def root():
    return {
        "message":     "EPL Match Outcome Predictor API",
        "version":     "1.0.0",
        "swagger_ui":  "/docs",
        "health":      "/health",
        "teams":       "/teams",
        "predict":     "POST /predict",
        "batch":       "POST /predict/batch",
    }


@app.get(
    "/health",
    response_model=HealthResponse,
    tags=["General"],
    summary="Health check — confirms artifacts are loaded and ready",
)
def health():
    try:
        teams       = pipeline.available_teams()
        generated   = pipeline._team_states.get('generated_at') if pipeline._team_states else None
        return HealthResponse(
            status          = "ok",
            model_loaded    = pipeline._model is not None,
            teams_available = len(teams),
            generated_at    = generated,
        )
    except Exception as e:
        raise HTTPException(status_code=503, detail=f"Service not ready: {str(e)}")


@app.get(
    "/teams",
    response_model=List[str],
    tags=["General"],
    summary="List all team names the model recognises",
)
def list_teams(
    search: Optional[str] = Query(
        default=None,
        description="Filter by partial name (case-insensitive). e.g. 'man' → Man City, Man United"
    )
):
    try:
        teams = pipeline.available_teams()
        if search:
            teams = [t for t in teams if search.lower() in t.lower()]
        return teams
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@app.post(
    "/predict",
    response_model=PredictResponse,
    tags=["Prediction"],
    summary="Predict the outcome of a single EPL match",
    responses={
        200: {"description": "Prediction returned successfully"},
        400: {"description": "Invalid input (bad date format, same team twice, etc.)"},
        500: {"description": "Model or artifact loading error"},
    },
)
def predict(req: PredictRequest):
    """
    Predict whether a Premier League match will end in a **Home Win (H)**,
    **Draw (D)**, or **Away Win (A)** — using only information available
    before the match starts.

    **Important:** Use `/teams` to check the exact team name spellings
    expected by the model (e.g. `"Man City"` not `"Manchester City"`).
    """
    try:
        result = _run_prediction(req)
        return JSONResponse(content=result)
    except FileNotFoundError as e:
        raise HTTPException(status_code=503, detail=str(e))
    except Exception as e:
        logging.error(f"Prediction error: {e}")
        raise HTTPException(status_code=500, detail=str(e))


@app.post(
    "/predict/batch",
    tags=["Prediction"],
    summary="Predict outcomes for up to 10 matches in one request",
    responses={
        200: {"description": "List of predictions returned"},
        400: {"description": "Invalid input"},
        500: {"description": "Internal error"},
    },
)
def predict_batch(req: BatchPredictRequest):
    """
    Predict outcomes for multiple matches in a single API call.
    Maximum **10 matches** per request. Each item in the response
    corresponds to the same-index item in the request.

    Useful for predicting a full match-week's fixtures at once.
    """
    try:
        results = []
        for match_req in req.matches:
            try:
                result = _run_prediction(match_req)
                results.append(result)
            except Exception as e:
                # Don't fail the whole batch for one bad match
                results.append({
                    "home_team":  match_req.home_team,
                    "away_team":  match_req.away_team,
                    "match_date": match_req.match_date,
                    "error":      str(e),
                })
        return JSONResponse(content={"predictions": results, "count": len(results)})
    except Exception as e:
        logging.error(f"Batch prediction error: {e}")
        raise HTTPException(status_code=500, detail=str(e))



